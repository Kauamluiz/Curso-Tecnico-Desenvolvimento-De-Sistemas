let numero, nome

    numero = Number(prompt("Digite um numero: "))

    switch(numero){

        case 0:

            alert("Zero!")
            break
        
        case 5:

            alert("Cinco!")
            break

        case 10:

            alert("Dez!")
            break

        default:

            alert("Nenhum!")

    }

    nome = prompt("Digite um nome: ")

    switch(nome){

        case 'Astrogildo':

            alert("Astrogildo, presente! :D")
            break

        case 'Ursula':

            alert("Ursula, presente! :D")
            break

        case 'Ermenegildo':

            alert("Ermenegildo, presente! :D")
            break

        default:

            alert("NO NAME!")

    }